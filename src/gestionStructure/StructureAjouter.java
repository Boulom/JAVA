    private void btnEnvoyerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnvoyerActionPerformed
        // test qui vÃ©rifie que les champs sont remplis

        if (txtcodeNafOUApe.getText().compareTo("") == 0) {
            JOptionPane.showMessageDialog(null, "Il faut saisir le code Naf ou Ape");
        } else {

            if (txtnumSiret.getText().compareTo("") == 0) {
                JOptionPane.showMessageDialog(null, "Il faut saisir le code postal");
            } else {
                if (txtraisonSoc.getText().compareTo("") == 0) {
                    JOptionPane.showMessageDialog(null, "Il faut saisir le code naf ou Ape");
                } else {
                    if (txtadresseRue.getText().compareTo("") == 0) {
                        JOptionPane.showMessageDialog(null, "Il faut saisir le numÃ©ro de siret");
                    } else {
                        if (txtadresseVille.getText().compareTo("") == 0) {
                            JOptionPane.showMessageDialog(null, "Il faut saisir le mail ");
                        } else {
                            if (txtcodePostal.getText().compareTo("") == 0) {
                                JOptionPane.showMessageDialog(null, "Il faut saisir le numÃ©ro de tÃ©lÃ©phone ");
                            } else {
                                if (txtMail.getText().compareTo("") == 0) {
                                    JOptionPane.showMessageDialog(null, "Il faut saisir la raison social ");
                                } else {
                                    if (txtTel.getText().compareTo("") == 0) {
                                        JOptionPane.showMessageDialog(null, "Il faut saisir l'adresse de la rue ");
                                    } else {

                                        //recherche du dernier id de structure donnÃ©
                                        int idMax = 0;
                                        for (Structure laStructure : NezDamesSerialisation.lesStructures) {
                                            if (laStructure.getIdStructure() > idMax) {
                                                idMax = laStructure.getIdStructure();
                                            }
                                        }
                                        PersonneStructure p=null;
                                        for(PersonneStructure unePersonne:lesPersonnesStructures){
                                            if (unePersonne.getNomPersonne().compareTo(jContact.getSelectedItem().toString())==0){
                                                p=unePersonne;
                                            }
                                        }
                                        
                                          PersonneStructure a=null;
                                        for(PersonneStructure unePersonne:lesPersonnesStructures){
                                            if (unePersonne.getNomPersonne().compareTo(jResponsable.getSelectedItem().toString())==0){
                                                a=unePersonne;
                                            }
                                        }
                                        
                                        // ajout d'un lieu Ã  la collection lesStructures
                                        NezDamesSerialisation.lesStructures.add(new Structure(idMax + 1,
                                                txtcodeNafOUApe.getText(), Integer.parseInt(txtnumSiret.getText()), 
                                                txtraisonSoc.getText(), txtadresseRue.getText(), txtadresseVille.getText(),
                                                Integer.parseInt(txtcodePostal.getText()), txtMail.getText(),
                                                Integer.parseInt(txtTel.getText()), p, a));
                                        JOptionPane.showMessageDialog(null, "Structure ajoutÃ©.");

                                        this.dispose();
                                    }
                                }
                            }
                        }
                    }
                }
            }
        }

    }//GEN-LAST:event_btnEnvoyerActionPerformed

